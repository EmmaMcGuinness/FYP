package com.project.growwithsunglow.ui.dashboard;

public class Titles {
    String planted;
    String daysAfter;
    String threeDays;
    String predicted;
    String actual;
    String gdhRequired;

    public Titles(String planted, String daysAfter, String threeDays, String predicted, String actual, String gdhRequired) {
        this.planted = planted;
        this.daysAfter = daysAfter;
        this.threeDays = threeDays;
        this.predicted = predicted;
        this.actual = actual;
        this.gdhRequired = gdhRequired;
    }
}
